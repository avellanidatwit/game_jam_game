def rgb_to_ansi(rgb):
    r, g, b = rgb
    return f"\033[38;2;{r};{g};{b}m"

def render_rgb_portrait(portrait, palette):

    for row in portrait:
        print("".join(rgb_to_ansi(palette[pixel]) + BLOCK for pixel in row) + RESET)